package edu.tongji.demo.Model;

public class WinnerList {
    private String code;
    private String name;
    private Double p_change;
    private Double amount;
    private Double buy;
    private Double bratio;
    private Double sell;
    private Double sration;
    private String reason;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getP_change() {
        return p_change;
    }

    public void setP_change(Double p_change) {
        this.p_change = p_change;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public Double getBuy() {
        return buy;
    }

    public void setBuy(Double buy) {
        this.buy = buy;
    }

    public Double getBratio() {
        return bratio;
    }

    public void setBratio(Double bratio) {
        this.bratio = bratio;
    }

    public Double getSell() {
        return sell;
    }

    public void setSell(Double sell) {
        this.sell = sell;
    }

    public Double getSration() {
        return sration;
    }

    public void setSration(Double sration) {
        this.sration = sration;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }
}
